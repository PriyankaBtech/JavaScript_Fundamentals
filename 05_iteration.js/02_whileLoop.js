/***************************** while Loop ***************************/


let index = 0
while (index <= 20) {
    console.log(`Value of index ${index}`)
    index = index + 2    
}

// loop over Array

let fruits = ["apple", "mango", "orange", "pineapple"]

let i = 0
while (i < fruits.length) {
    console.log(`Name of fruit is ${fruits[i]}`)
    i = i + 1    
}


/********************************* Do-while Loop ************************************/

let marks = 11

do {
    console.log(`My marks is ${marks}`)
    marks ++

} while (marks <= 10);



